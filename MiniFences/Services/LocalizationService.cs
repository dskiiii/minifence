namespace MiniFences.Services;

public sealed class LocalizationService
{
    public const string English = "en";
    public const string Chinese = "zh-CN";

    private string _language = English;

    public string Language
    {
        get => _language;
        set => _language = NormalizeLanguage(value);
    }

    public bool IsChinese => string.Equals(Language, Chinese, StringComparison.OrdinalIgnoreCase);

    public static string NormalizeLanguage(string? language)
    {
        return string.Equals(language, Chinese, StringComparison.OrdinalIgnoreCase) ||
               string.Equals(language, "zh", StringComparison.OrdinalIgnoreCase) ||
               string.Equals(language, "cn", StringComparison.OrdinalIgnoreCase)
            ? Chinese
            : English;
    }

    public string T(string key)
    {
        return IsChinese && ChineseText.TryGetValue(key, out var zh)
            ? zh
            : EnglishText.TryGetValue(key, out var en)
                ? en
                : key;
    }

    public static IReadOnlyList<string> GetMissingChineseKeys()
    {
        return EnglishText.Keys
            .Where(key => !ChineseText.ContainsKey(key))
            .OrderBy(key => key, StringComparer.Ordinal)
            .ToArray();
    }

